package com.github.cheshi_mantu;

public class Abbreviations {
    public static void main(String[] args) {
        int result = 1;
        System.out.println("Initial value of result: " + result);

        result++;
        System.out.println("Result of 'result++': " + result);

        result--;
        System.out.println("Result of 'result--': " + result);

        result += 2;
        System.out.println("Result of 'result += 2': " + result);

        result -= 2;
        System.out.println("Result of 'result -= 3': " + result);

        result *= 20;
        System.out.println("Result of 'result *= 2': " + result);

        result /= 2;
        System.out.println("Result of 'result /= 2': " + result);



    }
}
